#!/bin/bash
set -euo pipefail

CONFIG_HOME="${XDG_CONFIG_HOME:-$HOME/.config}"

KITTY_DIR="$CONFIG_HOME/kitty"
KITTY_THEMES_DIR="$KITTY_DIR/themes"
KITTY_LOCAL_THEME_FILE="$KITTY_DIR/theme.local.conf"
EZA_DIR="$CONFIG_HOME/eza"
EZA_LOCAL_THEME_FILE="$EZA_DIR/theme.local.sh"
VSCODE_USER_DIR="$CONFIG_HOME/Code/User"
VSCODIUM_USER_DIR="$CONFIG_HOME/VSCodium/User"
CODE_OSS_USER_DIR="$CONFIG_HOME/Code - OSS/User"

NVIM_THEME_FILE="$CONFIG_HOME/nvim/lua/config/theme_local.lua"
LEGACY_NVIM_THEME_FILE="$CONFIG_HOME/nvim/lua/config/theme.local.lua"
RANGER_COLORSCHEMES_DIR="$CONFIG_HOME/ranger/colorschemes"
RANGER_CURRENT_THEME_FILE="$RANGER_COLORSCHEMES_DIR/catppuccin_current.py"
SWAY_DIR="$CONFIG_HOME/sway"
SWAY_CURRENT_THEME_FILE="$SWAY_DIR/theme.local.conf"
SWAYLOCK_DIR="$CONFIG_HOME/swaylock"
SWAYLOCK_CURRENT_THEME_FILE="$SWAYLOCK_DIR/current.conf"
ZATHURA_DIR="$CONFIG_HOME/zathura"
ZATHURA_CURRENT_THEME_FILE="$ZATHURA_DIR/catppuccin-current"
ROFI_DIR="$CONFIG_HOME/rofi"
ROFI_LOCAL_THEME_FILE="$ROFI_DIR/theme.local.rasi"
WAYBAR_DIR="$CONFIG_HOME/waybar"
WAYBAR_LOCAL_THEME_FILE="$WAYBAR_DIR/theme.local.css"
MUTT_DIR="$CONFIG_HOME/mutt"
MUTT_LOCAL_THEME_FILE="$MUTT_DIR/theme.local.muttrc"

DEFAULT_THEME="Catppuccin-Mocha"

normalize_theme() {
    case "$1" in
        latte|Latte|Catppuccin-Latte) echo "Catppuccin-Latte" ;;
        frappe|Frappe|Catppuccin-Frappe) echo "Catppuccin-Frappe" ;;
        macchiato|Macchiato|Catppuccin-Macchiato) echo "Catppuccin-Macchiato" ;;
        mocha|Mocha|Catppuccin-Mocha) echo "Catppuccin-Mocha" ;;
        *) return 1 ;;
    esac
}

theme_to_flavour() {
    case "$1" in
        Catppuccin-Latte) echo "latte" ;;
        Catppuccin-Frappe) echo "frappe" ;;
        Catppuccin-Macchiato) echo "macchiato" ;;
        Catppuccin-Mocha) echo "mocha" ;;
        *) return 1 ;;
    esac
}

current_kitty_theme() {
    if [ -f "$KITTY_LOCAL_THEME_FILE" ]; then
        sed -n 's|^include themes/\(Catppuccin-[A-Za-z]*\)\.conf$|\1|p' "$KITTY_LOCAL_THEME_FILE" | head -n 1
    fi
}

current_nvim_theme() {
    if [ -f "$NVIM_THEME_FILE" ]; then
        sed -n 's|^  flavour = "\([a-z]*\)",$|\1|p' "$NVIM_THEME_FILE" | head -n 1
    elif [ -f "$LEGACY_NVIM_THEME_FILE" ]; then
        sed -n 's|^  flavour = "\([a-z]*\)",$|\1|p' "$LEGACY_NVIM_THEME_FILE" | head -n 1
    fi
}

write_kitty_theme_file() {
    local theme="$1"
    local theme_file="$KITTY_THEMES_DIR/${theme}.conf"

    if [ ! -f "$theme_file" ]; then
        echo "Kitty theme not found: $theme_file" >&2
        exit 1
    fi

    mkdir -p "$KITTY_DIR"
    cat >"$KITTY_LOCAL_THEME_FILE" <<EOF
# Auto-generated by changeTheme.sh
include themes/${theme}.conf
EOF
}

write_nvim_theme_file() {
    local flavour="$1"
    mkdir -p "$(dirname "$NVIM_THEME_FILE")"
    cat >"$NVIM_THEME_FILE" <<EOF
return {
  flavour = "${flavour}",
  colorscheme = "catppuccin",
}
EOF

    if [ "$LEGACY_NVIM_THEME_FILE" != "$NVIM_THEME_FILE" ]; then
        cat >"$LEGACY_NVIM_THEME_FILE" <<EOF
return {
  flavour = "${flavour}",
  colorscheme = "catppuccin",
}
EOF
    fi
}

write_eza_theme_file() {
    local flavour="$1"
    mkdir -p "$EZA_DIR"
    ln -sfn "themes/catppuccin-${flavour}.sh" "$EZA_LOCAL_THEME_FILE"
}

write_vscode_theme_file() {
    local flavour="$1"
    local theme=""
    local icon_theme=""

    case "$flavour" in
        latte)
            theme="Catppuccin Latte"
            icon_theme="catppuccin-latte"
            ;;
        frappe)
            theme="Catppuccin Frappé"
            icon_theme="catppuccin-frappe"
            ;;
        macchiato)
            theme="Catppuccin Macchiato"
            icon_theme="catppuccin-macchiato"
            ;;
        mocha)
            theme="Catppuccin Mocha"
            icon_theme="catppuccin-mocha"
            ;;
        *)
            echo "Unsupported VS Code flavour: $flavour" >&2
            exit 1
            ;;
    esac

    for user_dir in "$VSCODE_USER_DIR" "$VSCODIUM_USER_DIR" "$CODE_OSS_USER_DIR"; do
        local settings_file tmp_file
        settings_file="$user_dir/settings.json"
        mkdir -p "$user_dir"

        if [ ! -f "$settings_file" ]; then
            cat >"$settings_file" <<EOF
{
  "workbench.colorTheme": "$theme"
}
EOF
            continue
        fi

        tmp_file="$(mktemp)"
        python3 - "$settings_file" "$tmp_file" "$theme" "$icon_theme" <<'PY'
import pathlib
import re
import sys

settings_path = pathlib.Path(sys.argv[1])
tmp_path = pathlib.Path(sys.argv[2])
theme = sys.argv[3]
icon_theme = sys.argv[4]
text = settings_path.read_text(encoding="utf-8")

settings = [
    ("workbench.colorTheme", theme),
    ("workbench.preferredDarkColorTheme", theme),
    ("workbench.preferredLightColorTheme", theme),
    ("workbench.preferredHighContrastColorTheme", theme),
    ("workbench.preferredHighContrastLightColorTheme", theme),
    ("workbench.iconTheme", icon_theme),
]

for key, value in settings:
    pattern = re.compile(r'("' + re.escape(key) + r'"\s*:\s*)(?:"[^"]*"|null|true|false|-?\d+(?:\.\d+)?)')
    if pattern.search(text):
        text = pattern.sub(lambda m: m.group(1) + '"' + value + '"', text, count=1)
        continue

    stripped = text.rstrip()
    idx = stripped.rfind("}")
    if idx == -1:
        text = '{\n  "' + key + '": "' + value + '"\n}\n'
    else:
        before = stripped[:idx].rstrip()
        if before.endswith("{"):
            insertion = '\n  "' + key + '": "' + value + '"\n'
        else:
            insertion = ',\n  "' + key + '": "' + value + '"\n'
        text = before + insertion + "}\n"

tmp_path.write_text(text, encoding="utf-8")
PY
        mv "$tmp_file" "$settings_file"
    done
}

write_ranger_theme_file() {
    local flavour="$1"
    mkdir -p "$RANGER_COLORSCHEMES_DIR"
    cat >"$RANGER_CURRENT_THEME_FILE" <<EOF
from .catppuccin_${flavour} import Scheme
EOF
}

write_sway_theme_file() {
    local flavour="$1"
    mkdir -p "$SWAY_DIR"
    cat >"$SWAY_CURRENT_THEME_FILE" <<EOF
include themes/catppuccin-${flavour}.conf
EOF
}

write_swaylock_theme_file() {
    local flavour="$1"
    mkdir -p "$SWAYLOCK_DIR"
    ln -sfn "themes/catppuccin-${flavour}.conf" "$SWAYLOCK_CURRENT_THEME_FILE"
}

write_rofi_theme_file() {
    local flavour="$1"
    mkdir -p "$ROFI_DIR"
    cat >"$ROFI_LOCAL_THEME_FILE" <<EOF
@import "themes/catppuccin-${flavour}.rasi"
EOF
}

write_waybar_theme_file() {
    local flavour="$1"
    mkdir -p "$WAYBAR_DIR"
    cat >"$WAYBAR_LOCAL_THEME_FILE" <<EOF
@import "themes/catppuccin-${flavour}.css";
EOF
}

write_mutt_theme_file() {
    local flavour="$1"
    mkdir -p "$MUTT_DIR"
    case "$flavour" in
        latte)
            cat >"$MUTT_LOCAL_THEME_FILE" <<EOF
source ~/.config/mutt/themes/catppuccin-latte.muttrc
EOF
            ;;
        frappe|macchiato|mocha)
            cat >"$MUTT_LOCAL_THEME_FILE" <<EOF
source ~/.config/mutt/themes/catppuccin-dark.muttrc
EOF
            ;;
        *)
            echo "Unsupported NeoMutt flavour: $flavour" >&2
            exit 1
            ;;
    esac
}

write_zathura_theme_file() {
    local flavour="$1"
    mkdir -p "$ZATHURA_DIR"
    cat >"$ZATHURA_CURRENT_THEME_FILE" <<EOF
include catppuccin-${flavour}
EOF
}

reload_kitty() {
    pkill -USR1 kitty >/dev/null 2>&1 || true
}

reload_sway() {
    if command -v swaymsg >/dev/null 2>&1; then
        swaymsg reload >/dev/null 2>&1 || true
    fi
}

reload_waybar() {
    pkill -SIGUSR2 waybar >/dev/null 2>&1 || true
}

notify_theme() {
    local theme="$1"
    if command -v notify-send >/dev/null 2>&1; then
        notify-send -h string:x-canonical-private-synchronous:shared-theme \
            "Theme Updated" "${theme} for Kitty, NeoMutt, Neovim, Ranger, Waybar, Zathura, eza, and Sway" >/dev/null 2>&1 || true
    fi
}

set_theme() {
    local theme="$1"
    local flavour
    flavour="$(theme_to_flavour "$theme")"

    write_kitty_theme_file "$theme"
    write_nvim_theme_file "$flavour"
    write_eza_theme_file "$flavour"
    write_vscode_theme_file "$flavour"
    write_ranger_theme_file "$flavour"
    write_sway_theme_file "$flavour"
    write_swaylock_theme_file "$flavour"
    write_rofi_theme_file "$flavour"
    write_waybar_theme_file "$flavour"
    write_mutt_theme_file "$flavour"
    write_zathura_theme_file "$flavour"
    reload_kitty
    reload_sway
    reload_waybar
    notify_theme "$theme"
}

init_theme() {
    local theme="${1:-$DEFAULT_THEME}"
    local existing_kitty_theme
    local existing_nvim_flavour

    existing_kitty_theme="$(current_kitty_theme || true)"
    existing_nvim_flavour="$(current_nvim_theme || true)"

    if [ -n "$existing_kitty_theme" ]; then
        theme="$existing_kitty_theme"
    elif [ -n "$existing_nvim_flavour" ]; then
        theme="$(normalize_theme "$existing_nvim_flavour")"
    fi

    if [ ! -f "$KITTY_LOCAL_THEME_FILE" ]; then
        write_kitty_theme_file "$theme"
    fi

    if [ ! -f "$NVIM_THEME_FILE" ]; then
        write_nvim_theme_file "$(theme_to_flavour "$theme")"
    fi

    if [ ! -e "$EZA_LOCAL_THEME_FILE" ]; then
        write_eza_theme_file "$(theme_to_flavour "$theme")"
    fi

    write_vscode_theme_file "$(theme_to_flavour "$theme")"

    if [ ! -f "$RANGER_CURRENT_THEME_FILE" ]; then
        write_ranger_theme_file "$(theme_to_flavour "$theme")"
    fi

    if [ ! -f "$SWAY_CURRENT_THEME_FILE" ]; then
        write_sway_theme_file "$(theme_to_flavour "$theme")"
    fi

    if [ ! -e "$SWAYLOCK_CURRENT_THEME_FILE" ]; then
        write_swaylock_theme_file "$(theme_to_flavour "$theme")"
    fi

    if [ ! -f "$ROFI_LOCAL_THEME_FILE" ]; then
        write_rofi_theme_file "$(theme_to_flavour "$theme")"
    fi

    if [ ! -f "$WAYBAR_LOCAL_THEME_FILE" ]; then
        write_waybar_theme_file "$(theme_to_flavour "$theme")"
    fi

    if [ ! -f "$MUTT_LOCAL_THEME_FILE" ]; then
        write_mutt_theme_file "$(theme_to_flavour "$theme")"
    fi

    if [ ! -f "$ZATHURA_CURRENT_THEME_FILE" ]; then
        write_zathura_theme_file "$(theme_to_flavour "$theme")"
    fi
}

rofi_select() {
    local selection
    selection="$(
        printf '%s\n' \
            "🌻 Latte" \
            "🪴 Frappe" \
            "🌺 Macchiato" \
            "🌿 Mocha" |
            rofi -dmenu -i -p "Theme" -theme "${CONFIG_HOME}/rofi/config.rasi"
    )"

    case "$selection" in
        "🌻 Latte") set_theme "Catppuccin-Latte" ;;
        "🪴 Frappe") set_theme "Catppuccin-Frappe" ;;
        "🌺 Macchiato") set_theme "Catppuccin-Macchiato" ;;
        "🌿 Mocha") set_theme "Catppuccin-Mocha" ;;
        "") exit 0 ;;
        *) echo "Unknown selection: $selection" >&2; exit 1 ;;
    esac
}

case "${1:-}" in
    --init)
        if [ -n "${2:-}" ]; then
            init_theme "$(normalize_theme "$2")"
        else
            init_theme
        fi
        ;;
    --set)
        if [ -z "${2:-}" ]; then
            echo "Usage: changeTheme.sh --set <latte|frappe|macchiato|mocha>" >&2
            exit 1
        fi
        set_theme "$(normalize_theme "$2")"
        ;;
    *)
        rofi_select
        ;;
esac
