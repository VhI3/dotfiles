#!/bin/bash
set -euo pipefail

CONFIG_HOME="${XDG_CONFIG_HOME:-$HOME/.config}"

KITTY_DIR="$CONFIG_HOME/kitty"
KITTY_THEMES_DIR="$KITTY_DIR/themes"
KITTY_LOCAL_THEME_FILE="$KITTY_DIR/theme.local.conf"

NVIM_THEME_FILE="$CONFIG_HOME/nvim/lua/config/theme_local.lua"
LEGACY_NVIM_THEME_FILE="$CONFIG_HOME/nvim/lua/config/theme.local.lua"

DEFAULT_THEME="Catppuccin-Mocha"

normalize_theme() {
    case "$1" in
        latte|Latte|Catppuccin-Latte) echo "Catppuccin-Latte" ;;
        frappe|Frappe|Catppuccin-Frappe) echo "Catppuccin-Frappe" ;;
        macchiato|Macchiato|Catppuccin-Macchiato) echo "Catppuccin-Macchiato" ;;
        mocha|Mocha|Catppuccin-Mocha) echo "Catppuccin-Mocha" ;;
        *) return 1 ;;
    esac
}

theme_to_flavour() {
    case "$1" in
        Catppuccin-Latte) echo "latte" ;;
        Catppuccin-Frappe) echo "frappe" ;;
        Catppuccin-Macchiato) echo "macchiato" ;;
        Catppuccin-Mocha) echo "mocha" ;;
        *) return 1 ;;
    esac
}

current_kitty_theme() {
    if [ -f "$KITTY_LOCAL_THEME_FILE" ]; then
        sed -n 's|^include themes/\(Catppuccin-[A-Za-z]*\)\.conf$|\1|p' "$KITTY_LOCAL_THEME_FILE" | head -n 1
    fi
}

current_nvim_theme() {
    if [ -f "$NVIM_THEME_FILE" ]; then
        sed -n 's|^  flavour = "\([a-z]*\)",$|\1|p' "$NVIM_THEME_FILE" | head -n 1
    elif [ -f "$LEGACY_NVIM_THEME_FILE" ]; then
        sed -n 's|^  flavour = "\([a-z]*\)",$|\1|p' "$LEGACY_NVIM_THEME_FILE" | head -n 1
    fi
}

write_kitty_theme_file() {
    local theme="$1"
    local theme_file="$KITTY_THEMES_DIR/${theme}.conf"

    if [ ! -f "$theme_file" ]; then
        echo "Kitty theme not found: $theme_file" >&2
        exit 1
    fi

    mkdir -p "$KITTY_DIR"
    cat >"$KITTY_LOCAL_THEME_FILE" <<EOF
# Auto-generated by changeTheme.sh
include themes/${theme}.conf
EOF
}

write_nvim_theme_file() {
    local flavour="$1"
    mkdir -p "$(dirname "$NVIM_THEME_FILE")"
    cat >"$NVIM_THEME_FILE" <<EOF
return {
  flavour = "${flavour}",
  colorscheme = "catppuccin",
}
EOF

    if [ "$LEGACY_NVIM_THEME_FILE" != "$NVIM_THEME_FILE" ]; then
        cat >"$LEGACY_NVIM_THEME_FILE" <<EOF
return {
  flavour = "${flavour}",
  colorscheme = "catppuccin",
}
EOF
    fi
}

reload_kitty() {
    pkill -USR1 kitty >/dev/null 2>&1 || true
}

notify_theme() {
    local theme="$1"
    if command -v notify-send >/dev/null 2>&1; then
        notify-send -h string:x-canonical-private-synchronous:shared-theme \
            "Theme Updated" "${theme} for Kitty and Neovim" >/dev/null 2>&1 || true
    fi
}

set_theme() {
    local theme="$1"
    local flavour
    flavour="$(theme_to_flavour "$theme")"

    write_kitty_theme_file "$theme"
    write_nvim_theme_file "$flavour"
    reload_kitty
    notify_theme "$theme"
}

init_theme() {
    local theme="${1:-$DEFAULT_THEME}"
    local existing_kitty_theme
    local existing_nvim_flavour

    existing_kitty_theme="$(current_kitty_theme || true)"
    existing_nvim_flavour="$(current_nvim_theme || true)"

    if [ -n "$existing_kitty_theme" ]; then
        theme="$existing_kitty_theme"
    elif [ -n "$existing_nvim_flavour" ]; then
        theme="$(normalize_theme "$existing_nvim_flavour")"
    fi

    if [ ! -f "$KITTY_LOCAL_THEME_FILE" ]; then
        write_kitty_theme_file "$theme"
    fi

    if [ ! -f "$NVIM_THEME_FILE" ]; then
        write_nvim_theme_file "$(theme_to_flavour "$theme")"
    fi
}

rofi_select() {
    local selection
    selection="$(
        printf '%s\n' \
            "🌻 Latte" \
            "🪴 Frappe" \
            "🌺 Macchiato" \
            "🌿 Mocha" |
            rofi -dmenu -i -p "Theme" -theme "${CONFIG_HOME}/rofi/config.rasi"
    )"

    case "$selection" in
        "🌻 Latte") set_theme "Catppuccin-Latte" ;;
        "🪴 Frappe") set_theme "Catppuccin-Frappe" ;;
        "🌺 Macchiato") set_theme "Catppuccin-Macchiato" ;;
        "🌿 Mocha") set_theme "Catppuccin-Mocha" ;;
        "") exit 0 ;;
        *) echo "Unknown selection: $selection" >&2; exit 1 ;;
    esac
}

case "${1:-}" in
    --init)
        if [ -n "${2:-}" ]; then
            init_theme "$(normalize_theme "$2")"
        else
            init_theme
        fi
        ;;
    --set)
        if [ -z "${2:-}" ]; then
            echo "Usage: changeTheme.sh --set <latte|frappe|macchiato|mocha>" >&2
            exit 1
        fi
        set_theme "$(normalize_theme "$2")"
        ;;
    *)
        rofi_select
        ;;
esac
